    /**
     * Processes requests for both HTTP <code>GET</code> and <code>POST</code>
     * methods.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    protected void processRequest(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException, SQLException {
        response.setContentType("text/html;charset=UTF-8");
        HttpSession session=request.getSession(true);
                String admin=(String)session.getAttribute("Admin");
                String passc=request.getParameter("passc");
                String pass1=request.getParameter("pass1");
                try{
            Connection connect=CP.createC();
            System.out.println(connect.getClass());
            Statement s1=connect.createStatement();
            String query="UPDATE adminlogin SET password='"+pass1+"'WHERE username='"+admin+"' AND password='"+passc+"'";
            int row=s1.executeUpdate(query);
            if (row>0) {
                
                response.sendRedirect("AdminHome.jsp");
            }
            else
            {
                response.sendRedirect("AdminPassChange.jsp");
            }
            s1.close();
            connect.close();
        }
        catch(SQLException esql){
            System.out.println(esql);
        }
    }

    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
